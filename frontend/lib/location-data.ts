export function getCountryLabel(code: string) {
  return countryLabels[code] ?? code;
}

export const countries: CountryOption[] = countryCodes
  .map((code) => ({
    value: code,
    label: getCountryLabel(code),
  }))
  .sort((first, second) => {
    if (first.value === "SA") {
      return -1;
    }

    if (second.value === "SA") {
      return 1;
    }

    return first.value.localeCompare(second.value);
  });

