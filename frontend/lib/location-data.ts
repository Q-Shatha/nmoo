const countryDisplayNames = new Intl.DisplayNames(["ar"], { type: "region" });

export function getCountryLabel(code: string) {
  if (code === "XK") {
    return "كوسوفو";
  }

  return countryDisplayNames.of(code) ?? code;
}

export const countries: CountryOption[] = countryCodes
  .map((code) => ({
    value: code,
    label: getCountryLabel(code),
  }))
  .sort((first, second) => {
    if (first.value === "SA") {
      return -1;
    }

    if (second.value === "SA") {
      return 1;
    }

    return first.label.localeCompare(second.label, "ar");
  });

